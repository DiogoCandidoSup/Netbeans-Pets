    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
      //if(!txtNome.isEmpty() == 0)
      //{
        Animal a = new Animal();
                
        a.setNome(txtNome.getText()); //nome
        a.setRaca(txtRaca.getText()); //raca
        if(rdbMacho.isSelected())  //sexo
        {
            a.setSexo("Macho");
        }
        else
        {
            a.setSexo("Femea");
        }
        
        a.setCor(txtCor.getText()); //cor
        
        switch(cbxTipo.getSelectedIndex()) //tipo
        {
            case 0:
                a.setTipo("Cachorro");
                break;
        }
        
        a.setAno(Integer.parseInt(txtAno.getText())); //data nasc
        
        bdanimal.adicionar(a);
     //}
    }//GEN-LAST:event_btnSalvarActionPerformed

